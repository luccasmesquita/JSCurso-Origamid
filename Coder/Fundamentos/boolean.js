// tipos de dados, um deles é o boolean que me retorna se é true ou false
// o zero (0) é sempre falso / false
let isAtivo = false; // isso é uma variavel booleana com o valor falso
console.log(isAtivo);

isAtivo = true; // isso é uma variavel booleana com o valor true
console.log(isAtivo)

isAtivo = 1; //podemos usar um valor numerico para representar um dado booleano
console.log(!!isAtivo); // colocando o "!" é equivalente é conferir se é negativo, colocando duas vezes verifica o valor correto

console.log('Os verdadeiros'); //todos os numeros inteiros é verdadeiro
console.log(!!3);
console.log(!!-1);
console.log(!!' '); //um espaço vazio é verdadeiro
console.log(!![]); //um array é verdadeiro 
console.log(!!{}); //um objeto é verdadeiro 

console.log('Os Falsos'); //todos os numeros inteiros é verdadeiro
console.log(!!0);
console.log(!!''); //uma string vazia é falso
console.log(!!null);
console.log(!!NaN);
console.log(!!undefined);







